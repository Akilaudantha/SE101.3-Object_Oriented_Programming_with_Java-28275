package com.mycompany.q3;

public class Q3 {

    public static void main(String[] args) {
        
        for (int i=0; i<5; i++)
        {
            System.out.println("Executing Loop "+i);
        }
        
        System.out.println("\n");
        int t=0;
        
        while (t<5)
        {
            System.out.println("Executing Loop "+t);
            t++;
        }
        
    }
}
